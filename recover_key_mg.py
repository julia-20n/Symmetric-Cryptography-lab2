def preprocess_text(text):
    return ''.join([c.lower() for c in text if c.lower() in ALPHABET])

def split_text_into_blocks(ciphertext, r):
    blocks = ['' for _ in range(r)]
    for i, char in enumerate(ciphertext):
        blocks[i % r] += char
    return blocks

def compute_mi(block_freqs, lang_freqs_shifted):
    return sum(block_freqs.get(ALPHABET[(i + g) % ALPHABET_SIZE], 0) * lang_freqs_shifted[i]
               for i, g in enumerate([0]*ALPHABET_SIZE))

def determine_key_letter_by_mg(block):
    block_count = Counter(block)
    max_m = -1
    best_g = 0
    lang_freq = [RUS_FREQ[ch] for ch in ALPHABET]

    for g in range(ALPHABET_SIZE):
        shifted_freq = lang_freq[-g:] + lang_freq[:-g]
        m_val = sum(block_count.get(ALPHABET[i], 0) * shifted_freq[i] for i in range(ALPHABET_SIZE))
        if m_val > max_m:
            max_m = m_val
            best_g = g

    return ALPHABET[best_g]

def recover_key_by_mg(ciphertext, r):
    blocks = split_text_into_blocks(ciphertext, r)
    key = ''.join(determine_key_letter_by_mg(block) for block in blocks)
    return key

